# Arithmetic Operators in Python
# Addition:         +
# Subtraction:      -
# Multiplication:   *
# Exponent:         **
# Division:         /
# Floor Division:   //
# Remainder:        %

x = 5
result = x + 10
print(result)

x = 5
result = x - 10
print(result)

x = 5
result = x * 10
print(result)

x = 5
result = x / 10
print(result)

x = 5
power = x ** 2
print(power)

x = 5
quotient = x // 2
print(quotient)

x = 5
remainder = x % 2
print(remainder)