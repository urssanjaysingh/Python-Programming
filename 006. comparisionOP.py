# comparision operator
'''
<   less than
>   greater than
==  equal
!=  not equal
>=  greater than or equal to
<=  less than or equal to
'''
number = 15
print(number < 10)

number = 15
print(number > 10)

number = 10
print(number == 10)

number = '10'
print(number != 10)

number = 10
print(number <= 10)

number = 10
print(number >= 10)