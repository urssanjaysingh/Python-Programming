# all lowercase character
text = "I like Python 3"
result = text.lower()
print(result)

# all uppercase character
text = "I like Python 3"
result = text.upper()
print(result)

# finding index
text = "I like Python 3"
result = text.find("Python")
print(result)

# replacing a string
text = "I like Python 3"
result = text.replace("Python 3", "C++")
print(result)
