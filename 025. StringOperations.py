# String operations
text1 = "Python"
text2 = "Programming"

# concatenating strings
result = text1 + " " + text2
print(result)

# repeating string 
text = "Python"
new_text = text * 3
print(new_text)

# ITERATING through strings
text = "Python"
for character in text:
    print(character)

# in operator
text = "Python"
print("yth" in text)
print("ont" in text)
